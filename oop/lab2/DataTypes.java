public class DataTypes{
    public static void main(String[] args) {
        // byte (-128, 127)        
        byte a = 10;

        // short (-32768, 32767)
        short b = 30000;

        char n = 'n';

        String name = "nut";

        double c = 10e5;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(n);
        System.out.println(name);
    }
}