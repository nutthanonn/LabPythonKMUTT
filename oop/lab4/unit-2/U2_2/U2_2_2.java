
public class U2_2_2 {
    public static void main(String[] args) {
        char case_val = 'A';
        switch (case_val) {
            case 'A':
                System.out.println('A');
                break;
            case 'B':
                System.out.println('B');
                break;
            case 'C':
                System.out.println('C');
                break;
            default:
                System.out.println(case_val);
                break;
        }
    }
}
